alian infeksi yang dilakukan di rumah sakit tersebut. Bank darah berperan menyediakan produk darah yang aman untuk pasien yang mengalami kekurangan darah seperti pada anemia, kekurangan trombosit, zat pembekuan darah. Pelayanan yang dapat dilakukan di laboratorium terpadu RSUI adalah :</p>
